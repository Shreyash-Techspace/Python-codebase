"""
Escape Sequence is used when we need to insert or add a certain special character in a string which cannot otherwise added
some special characters
1. \n : new/next line
2. \t : add tab (big space)
3. \\ : add single backslash
4. \' : insert a single quote inside a single-quoted string
5. \" : insert a double quotes inside a double-quoted string
and many more....
"""

